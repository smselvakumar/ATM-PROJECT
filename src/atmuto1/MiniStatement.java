    private void showBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showBtnActionPerformed
try {
    
    con = DriverManager.getConnection("jdbc:mysql://localhost:3306/atmdb", "root", "");
    Statement st = con.createStatement();
    String sql = "select * from transactiontbl where AccNum='"+MyAccNum+"'";
    ResultSet rs = st.executeQuery(sql);

     DefaultTableModel Mini= (DefaultTableModel) MiniStateMentTable.getModel();

     Mini.setRowCount(0);
    
     while (rs.next()) 
     {
        int AccNum = rs.getInt("Accnum");
        String Type = rs.getString("Type");
        String Date = rs.getString("Date/Time");
        String Amount = rs.getString("Amount");
        String strAccNum = Integer.toString(AccNum);
        Object[] tbData = {strAccNum, Type, Date, Amount};
        Mini.addRow(tbData);
        
    }
    rs.close();
    st.close();
    con.close();
} catch (Exception e) {
    e.printStackTrace();
}

    }//GEN-LAST:event_showBtnActionPerformed

